public class Coordinata {
    private int x;
    private int y;
    private int h;
    
    public Coordinata(int _x, int _y, int _h){
        this.x = _x;
        this.y = _y;
        this.h = _h;
    }

	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}

	public int getH() {
		return h;
	}
    
}
