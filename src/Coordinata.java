public class Coordinata {
    private String x;
    private String y;
    private String h;
    
    public Coordinata(String _x, String _y, String _h){
        this.x = _x;
        this.y = _y;
        this.h = _h;
    }

	public String getX() {
		return x;
	}
	public String getY() {
		return y;
	}

	public String getH() {
		return h;
	}
    
}
